def all_possible_values(arr):
    if len(arr) == 1: 
        return {arr[0]}
    subset = all_possible_values(arr[:-1])
    return {x + arr[-1] for x in subset} | {x * arr[-1] for x in subset} | {int(str(x) + str(arr[-1])) for x in subset}
 


def can_get(target, arr):
    if len(arr) == 1 : return target == arr[0]
    if target % arr[-1] == 0 and can_get(target // arr[-1], arr[:-1]): return True # logic for * and below for +
    if target > arr[-1] and can_get(target - arr[-1], arr[:-1]): return True
    s_target = str(target)
    s_last = str(arr[-1])
    if len(s_target) > len(s_last) and s_target.endswith(s_last) and can_get(int(s_target[:-len(s_last)]), arr[:-1]): return True 
    
    return False


