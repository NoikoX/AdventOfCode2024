import numpy as np


def is_safe(l):
    if l == sorted(l, reverse=True) or l == sorted(l):
        for i in range(len(l) - 1):
            if not(1 <= abs(l[i + 1] - l[i]) <= 3):
                return False
        return True
    return False


with open('input.txt', 'r') as file:
    t = 0
    for line in file.readlines():
        l = list(map(int, line.split()))
        if is_safe(l):
            t += 1
        else:
            for i in range(len(l)):
                l1 = l[:i] + l[i + 1:]
                if is_safe(l1):
                    t += 1
                    break
    print(t)
        
