t = 0
def safe(l):
    diffs = [x - y for x, y in zip(l, l[1:])]
    return all(1 <= x <= 3 for x in diffs) or all(-1 >= x >= -3 for x in diffs)

for r in open('input.txt'):
    l = list(map(int, r.split()))
    # if safe(l): t += 1 was the first one 
    if any(safe(l[:i] + l[i + 1:]) for i in range(len(l))):
        t += 1
        
